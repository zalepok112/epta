Утворення Січі
</P>

<P class="picture">
<IMG SRC="sich_mal1.gif" ALIGN="right" hspace="30" vspace="10" BORDER=0>
</P>

<P class="middle">
Постійна загроза нападу зовнішніх ворогів 
змушувала козаків будувати укріплення з дерев — засіки. 
З ними вчені пов'язують походження слова січ (згромадження січеного дерева).
Можливо існувало багато січей, 
але столицею запорізьких козаків була Запорізька Січ. 
Січ — це місто — фортеця. Її розташовували на річковому острові, 
щоб було важко дістатися її ворогам. 
Природні укріплення підсилювалися 10—13 метровим валом і ровом. 
Вал зміцнювався частоколом із баштами, де містилися бійниці. 
Усередині фортеці був майдан із православною церквою. 
Навколо церкви стояли довгі будинки — курені, 
де жили січовики, а також оселя старшини, канцелярія, ремісничі. 
Майстерні, торгові лавки тощо. Чисельність козаків і Січі постійно коливалася, 
але 2—3 тисячі людей мало бути постійно. Першою відомою Січчю вважається фортеця, 
зведена 1556 р. на о. Мала Хортиця українським князем Дмитром Вишневецьким. 
Восени 1557 р. її зруйнували орди кримського хана. 
</P>

<P class="picture">
<IMG src="sich_mal3.gif" ALIGN="right" hspace="10" vspace="10" BORDER=0>
</P>

<P class="middle">
Територія козацької держави називалась Землями війська Запорізького. 
Відомо 8 дніпровських січей. За внутрішнім устроєм 
козацька держава була своєрідним військовим товариством. 
Козацька держава — це християнсько — православна демократична республіка.
Кожен козак приписувався до куреня — військово — господарської одиниці на Січі (їх було 8). 
Назва куренів свідчила про походження козаків: Уманський, Корсунський тощо.
</P>

